package com.runtimePolymorphism;

public class Fruit {
	
	String sweet;
	String sour;
	String spicy;
	
	
	public String getSweet() {
		return sweet;
	}
	public void setSweet(String sweet) {
		this.sweet = sweet;
	}
	public String getSour() {
		return sour;
	}
	public void setSour(String sour) {
		this.sour = sour;
	}
	public String getSpicy() {
		return spicy;
	}
	public void setSpicy(String spicy) {
		this.spicy = spicy;
	}
	
	

}
